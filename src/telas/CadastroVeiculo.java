    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        String placa = txtPlaca.getText();
        String renavam = txtRenavam.getText();
        String chassi = txtChassi.getText();
        String marcaModelo = txtMarcaModelo.getText();
        
        int anoFab;
        int anoMod;
        
        try {
        anoFab = Integer.parseInt(txtAnoFab.getText());
        anoMod = Integer.parseInt(txtAnoMod.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, insira um ano válido.");
        return;
    }
        
        String observacoes = txtObservacoes.getText();
        
        String selectedItem = jComboBox2.getSelectedItem().toString();
        CarroceriaENUM carroceria = CarroceriaENUM.valueOf(selectedItem);
        
        String selectedItem2 = jComboBox1.getSelectedItem().toString();
        CategoriaENUM categoria = CategoriaENUM.valueOf(selectedItem2);
        
        Veiculo veiculo = new Veiculo(); //criando um novo veículo
    veiculo.setPlaca(placa);
    veiculo.setRenavam(renavam);
    veiculo.setChassi(chassi);
    veiculo.setMarcaModelo(marcaModelo);
    veiculo.setAnoFab(anoFab);
    veiculo.setAnoMod(anoMod);
    veiculo.setCategoriaENUM(categoria);
    veiculo.setCarroceriaENUM(carroceria);
    veiculo.setObservacoes(observacoes);
    
    //salvando o novo veículo do banco de dados através do VeiculoDAO
    VeiculoDAO veiculoDAO = new VeiculoDAO();
    veiculoDAO.salvar(veiculo);
        
    }//GEN-LAST:event_jButton3ActionPerformed

