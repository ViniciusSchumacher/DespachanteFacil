    //colocando os tipos de carroceria no combo box
    private void preencherJComboBox2() {
    jComboBox2.removeAllItems(); // Adicione esta linha para limpar o JComboBox
    CarroceriaENUM[] carrocerias = CarroceriaENUM.values();
    for (CarroceriaENUM carroceria : carrocerias) {
        jComboBox2.addItem(carroceria.name());
    }
}
    
    //colocando os tipos de categoria no combo box
    private void preencherJComboBox1() {
        jComboBox1.removeAllItems();
        CategoriaENUM[] categorias = CategoriaENUM.values();
        for (CategoriaENUM categoria : categorias) {
            jComboBox1.addItem(categoria.name());
        }
    }


    
    
